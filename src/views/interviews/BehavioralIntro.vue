          <section>
            <h2>Typical behavioral interview leveling expectations</h2>

            <p>
              Behavioral interviews is one of the interview formats where it is
              possible for interviewers to assess the candidate's maturity —
              from Junior Software Engineer all the way to extremely senior
              positions depending on the stories they tell, their responses and
              decisions made. Raw number of years of working experience doesn't
              necessarily equate to specific levels as some people mature faster
              than others. Leveling guidance is usually made with a
              consideration of raw years of experience and leveling
              expectations. The levels mentioned below are based on Google and
              Facebook's levels where L3 is the equivalent of Junior Software
              Engineers / fresh graduates.
            </p>

            <p>
              Understand the expected behaviors for the commonly-defined
              engineering levels and craft your responses ensuring they match
              your current level or the level you are targeting.
            </p>

            <div class="subsection">
              <h3>Junior Software Engineer (L3)</h3>
              <ul class="bullet-list">
                <li>
                  Takes direction from managers and/or senior members of the
                  team.
                </li>
                <li>
                  Executes on well-defined tasks without too much guidance.
                </li>
                <li>Primarily improves their own skills and craft.</li>
              </ul>
            </div>

            <div class="subsection">
              <h3>Software Engineer (L4)</h3>
              <ul class="bullet-list">
                <li>
                  Understands project purpose and helps to achieve the team's
                  objectives.
                </li>
                <li>
                  Splits larger projects (typically feature work) given to them
                  into smaller tasks for timely execution.
                </li>
                <li>
                  Suitably balance between delegating tasks and performing tasks
                  themselves.
                </li>
                <li>
                  Improves their own skills while possibly mentoring more junior
                  members, interns, or new hires.
                </li>
                <li>
                  Collaborates cross-functionally with guidance from senior
                  members of the team.
                </li>
              </ul>
            </div>

            <div class="subsection">
              <h3>Senior Software Engineer (L5)</h3>
              <ul class="bullet-list">
                <li>
                  Leads development on complex tasks and projects within the
                  team.
                  <ul class="nested-list">
                    <li>
                      Identifies well thought out solutions to ambiguous
                      projects of large scope
                    </li>
                    <li>
                      Breaks solution down into smaller projects and delegating
                      some of them to team members.
                    </li>
                  </ul>
                </li>
                <li>
                  Proactively seeks new ideas and directions to build the
                  product better.
                </li>
                <li>
                  Mentors multiple junior members of the team and help them to
                  improve by being a role model.
                </li>
                <li>
                  Works cross-functionally independently and drives complex,
                  ambiguous discussions.
                </li>
              </ul>
            </div>

            <div class="subsection">
              <h3>Staff Software Engineer (L6)</h3>
              <ul class="bullet-list">
                <li>
                  Understands business goals and advises managers and
                  organization leaders to achieve the goals.
                </li>
                <li>
                  Leads or greatly influences direction of a team of engineers.
                </li>
                <li>
                  Demonstrates domain expertise and is looked up to by other
                  engineers.
                </li>
                <li>
                  Conducts clear, long-term planning across teams/within the
                  organization and drives consensus.
                </li>
                <li>Mentors tech leads and junior members of the team.</li>
                <li>
                  Works on projects requiring interactions with engineers and
                  cross-functional partners across multiple teams.
                </li>
              </ul>
            </div>

            <div class="subsection">
              <h3>Senior Staff Software Engineer (L7) and beyond</h3>
              <ul class="bullet-list">
                <li>
                  Owns organization/company-wide business/engineering goals and
                  delivers on them.
                </li>
                <li>
                  Influences or leads product and engineering roadmaps within
                  the organization/company.
                </li>
                <li>
                  Leads solution development and delivery for highly complex and
                  ambiguous areas.
                </li>
              </ul>
