          <section>
            <h2>Behavioral interview signals and evaluation criteria</h2>

            <p>
              In big tech companies where structured behavioral interviews are
              conducted, interviewers are often trained to conduct behavioral
              interviews in a specific manner.
            </p>

            <p>
              They are provided specific guidelines, which commonly include:
            </p>

            <ol class="numbered-list">
              <li>
                <strong>Evaluation criteria:</strong> Typically split into
                certain major categories such as Communication, Proactivity,
                etc., which they need to evaluate candidates on. Each category
                will typically also include specific guidance as to how to grade
                a candidate as good, poor, average, etc.
              </li>
              <li>
                <strong>Level expectations:</strong> Interviewers are typically
                asked to provide an expected level after interviewing the
                candidate. To ensure consistency between interviewers, a
                leveling guide is also provided.
              </li>
            </ol>

            <p>
              Below, we present the typical behavioral interview evaluation
              criteria as well as the typical level expectations for most
              companies. The recommended preparation strategy provides
              suggestions on how you might prepare based on these criteria.
            </p>

            <div class="subsection">
              <h3>The interviewer's perspective</h3>

              <p>
                Behavioral interviewers are typically required to give a rating
                on a few broad categories, such as Collaboration, Proactivity,
                etc. For each evaluation category, they are provided with
                criteria as to what should constitute a good or bad rating.
                Ratings are commonly done on a 5-point rating scale, such as one
                of Very good / Good / Sufficient / Poor / Very Poor.
              </p>

              <p>
                In addition to these, interviewers can also mark if there wasn't
                sufficient information to make a judgment, or if they didn't
                manage to ask a question that touches on a specific signal as
                they ran out of time.
              </p>

              <p>
                Ratings have to be done with regard to the situation's
                complexity. A more difficult or complex situation handled well
                will typically receive a higher rating and leveling
                recommendation.
              </p>

              <p>
                At the end of the interview they reconcile all the ratings and
                qualitative feedback to decide:
              </p>

              <ul class="bullet-list">
                <li>Whether to pass or fail the candidate</li>
                <li>If passed, what level the candidate should receive</li>
              </ul>

              <p>
                The implication of the above is that we can typically expect
                behavioral interviews in big tech to have some structure, as
                interviewers will try to ask at least 1 question from each
                category to evaluate candidates.
              </p>

              <p>
                Additionally, it is important for candidates to avoid rambling
                on for long periods of time on one question as interviewers may
                run out of time. Poor communication or missing the point of the
                question could also constitute insufficient signals.
              </p>

              <p>
                Apart from that, interviewers are typically encouraged to
                utilize follow-up questions to dig deeper into the candidate's
                actual motivations and understanding behind their surface-level
                actions:
              </p>

              <ol class="numbered-list">
                <li>Why do you think you did (insert action)?</li>
                <li>Why did you not do (insert action)?</li>
                <li>How would you do things differently in hindsight?</li>
              </ol>

              <p>
                Hence, candidates should also prepare a solid understanding of
                each alternative mode of action's advantages, disadvantages, and
                reflect on their intrinsic motivations.
              </p>
            </div>
          </section>

