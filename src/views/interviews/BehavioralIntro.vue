          <section>
            <h2>Recommended preparation strategy</h2>

            <p>
              As mentioned above, based on the evaluation criteria we can
              typically expect behavioral interviews in big tech to have some
              structure, as interviewers will try to ask at least 1 question
              from each category to evaluate candidates.
            </p>

            <p>
              Apart from these questions, we can also expect general but common
              questions such as "Tell me about yourself" or "Why join this
              company".
            </p>

            <p>
              There is quite a lot of variance in terms of the questions that
              could be asked. While it is too time consuming to prepare for all
              of them, we can categorize the 80% most common questions into
              themes and tackle them methodically, per our following guides:
            </p>

            <ol class="numbered-list">
              <li>"Tell me about yourself..."</li>
              <li>"Walk me through your resume..."</li>
              <li>"Why join this team or company?"</li>
              <li>
                "Tell me about a time when..." (categorized into the following
                themes)
                <ul class="nested-list">
                  <li>Driving results and problem solving</li>
                  <li>Collaboration</li>
                  <li>Growth mindset</li>
                  <li>Adaptability and flexibility</li>
                </ul>
              </li>
              <li>"Do you have any questions for me?"</li>
            </ol>

            <div class="subsection">
              <h3>
                Prepare a few good, general stories based on common themes
              </h3>

              <p>
                The general strategy to tackle the large variance of "Tell me
                about a time when..." questions to be asked is to prepare 3-5
                general and well-thought out stories that could be used to
                address multiple required traits. For instance, an experience
                collaborating extensively with others could display your traits
                in communication, team work, leadership, conflict management,
                etc. Whenever an interviewer asks you a question, you could run
                through the stories you have prepared and adapt them to answer
                the specific question.
              </p>

              <p>
                Here are some tips to select and prepare good project
                experiences as stories:
              </p>

              <ul class="bullet-list">
                <li>
                  Check against the evaluation criteria above and try to pick
                  broad experiences which can be used to address multiple
                  criteria. Then, write down short bullet points of how you
                  exhibited behavior that fulfilled required traits.
                </li>
                <li>
                  Choose experiences that showcase your level expectations. For
                  instance, if you are applying for a Staff Software Engineer
                  position, ensure that your experiences exhibit your influence
                  on the direction of entire teams.
                </li>
                <li>
                  Do not reuse the same stories too much in the same interview
                  loop. Interviewers tend to discuss candidate performance
                  together, hence overusing the same story could come off as
                  repetitive and insufficient breadth of experience.
                </li>
                <li>
                  As mentioned above, interviewers tend to prioritize follow-up
                  questions to understand the candidate's true psyche,
                  motivations and understanding. For each story, prepare a solid
                  understanding of each alternative mode of action's advantages,
                  disadvantages, and reflect on your intrinsic motivations.
                </li>
              </ul>
            </div>

            <div class="subsection">
              <h3>Structure initial answers with the STAR framework</h3>

              <p>
                Having prepared a story you could use, you need to structure
                them well when answering specific questions. One requirement
                interviewers are always asked to look out for is the candidates
                actual ability to communicate during the interview (as opposed
                to what they claim), which is evaluated as follows:
              </p>

              <ul class="bullet-list">
                <li>Well-organized and easy to follow thought process</li>
                <li>
                  Good intuition on which details should be elaborated and which
                  to keep simple
                </li>
                <li>Leaves others clear on purpose and actions</li>
                <li>Demonstrates active listening</li>
              </ul>

              <p>
                To achieve the above, you need to structure your answers well to
                ensure that it is concise and gets to the point below 3 minutes.
                One rough guideline you could use is a framework like STAR:
              </p>

              <ul class="bullet-list">
                <li>
                  <strong>Situation:</strong> Present basic details of the
                  scenario, just enough to understand the challenge you were in.
                  (Do not spend too much time on this)
                </li>
                <li>
                  <strong>Task:</strong> What did you need to achieve or do in
                  the scenario? (Do not spend too much time on this)
                </li>
                <li>
                  <strong>Action:</strong> What did you do? This is where you
                  exhibit required traits based on the approach you took to
                  solve the challenge or situation.
                </li>
                <li>
                  <strong>Result:</strong> What was the outcome of your actions?
                </li>
              </ul>

              <p>
                Here is an example of applying STAR to answering a behavioral
                interview question "Tell me about a time you had to manage
                multiple conflicting priorities and how you handled it":
              </p>

              <div class="example-section">
                <h4>Situation</h4>
                <p>
                  In my current job as Front End Engineer in a startup, I was
                  once in a situation where I had to deliver several important
                  features for an e-commerce campaign at very short notice,
                  since different teams were making feature requests at the
                  time.
                </p>

                <h4>Task</h4>
                <p>
                  As I recognized that it was not possible for me to achieve all
                  of them with good quality and also in a timely manner. I had
                  to find a way to deconflict their priorities.
                </p>

                <h4>Action</h4>
                <p>
                  • What I did was to arrange a quick meeting with all relevant
                  product and engineering stakeholders to co-prioritize and
                  assign the appropriate resources to support this project.<br />
                  • I listed the features requested from every team and worked
                  with all the relevant stakeholders to identify each feature's
                  contribution to business objectives, while also roughly
                  estimating the engineering effort for each one.<br />
                  • This helped us to deprioritize features that had very high
                  engineering effort but little contribution to the
                  objectives.<br />
                  • For projects that were not realistically possible to achieve
                  within the timelines, we were able to negotiate for more
                  engineering resources to delegate these feature work to.<br />
                  • After that, to ensure my own timelines were met, I broke
                  down my features into smaller tasks and planned them into
                  daily and weekly milestones, reviewing my progress regularly
                  with the team.
                </p>

                <h4>Result</h4>
                <p>
                  With this co-prioritization and planning effort, I was able to
                  achieve all the required features by the stipulated timeline.
                </p>
              </div>
            </div>

            <div class="subsection">
              <h3>Important warning about your answers</h3>

              <p>
                A final note — in order for the interviewer to follow your
                thought process or understand how brilliant your solutions were,
                it is VERY important for them to understand the context, purpose
                and situation. The problem or situation should be described very
                clearly and it should assume NO prior understanding. Many
                candidates fall into the trap of assuming the interviewer can
                follow their stories without giving enough context, especially
                when problems are domain-specific like fintech or blockchain. In
                such situations, interviewers might end up marking you as having
                bad communication, or rate the category as having insufficient
                signals.
              </p>
            </div>

            <div class="subsection">
              <h3>Practice makes perfect</h3>

              <p>
                After preparing your stories and having a rough sense of STAR,
                practice using your prepared stories and the STAR format to
                answer our list of 50 common behavioral interview questions.
                Instead of memorizing the answers, practice just answering them
                verbally each time. This allows you to cement your stories and
                familiarize with STAR.
              </p>
            </div>
          </section>

          <section>
            <h2>How best to conduct yourself in a behavioral interview</h2>

            <div class="subsection">
              <h3>Understanding the interviewer's intention</h3>

              <p>
                Some candidates think of behavioral interviews as a test or an
                exam, where you have to study or memorize to get the right
                points or answer. That is not what the interviewer is looking
                out for. To understand how interviewers themselves feel, try to
                picture yourself interviewing someone to join a project you are
                actually currently working on. That is, put yourself in the
                hiring manager's position.
              </p>

              <p>
                A behavioral interview usually happens after the technical
                rounds are completed. Candidates that have passed the technical
                rounds are considered technically competent. However, as a
                manager, you hardly really know this stranger (the candidate).
                Interviews are a conversation for you to get to know this
                stranger as a person. It's just like a typical chat in a social
                setting, somewhat like a speed date, except the interviewer
                needs to get to know you enough to decide if you can work well.
              </p>

              <p>
                Understandably, as an interviewer, you would generally try to
                sieve out:
              </p>

              <ul class="bullet-list">
                <li>
                  Is this candidate going to be a poor performer? e.g. Often
                  miss on deadlines, not accountable, not driven, gets into
                  regular conflict with others, communicates poorly, does not
                  take feedback well. Are they lying or exaggerating on their
                  CV?
                </li>
                <li>
                  Is the candidate going to be a superstar, driven teammate?
                  e.g. communicates well, resourceful and independent in solving
                  problems, able to manage stakeholders, proactive and works
                  hard in enacting new initiatives to drive the team forward.
                </li>
                <li>
                  Is the candidate going to be easy to manage? e.g. takes
                  feedback well, likable, sociable and does not give you
                  problems.
                </li>
              </ul>

              <p>
                If a candidate were to recite answers back to you or appear
                stoic and unexcited, would you hire them into your team?
                Probably not. For interviewers, it's as much about seeing you as
                your true, natural self as it is to know if your thinking
                process is aligned with their goals.
              </p>
            </div>

            <div class="subsection">
              <h3>One size fits most</h3>

              <p>
                Interviewers always mention team "fit" as a criteria for
                evaluating you. But there are so many different teams out there
                — how would you know how to conduct yourself to display fit?
              </p>

              <p>
                One way to do so is to ask the recruiter or other interviewers
                in prior rounds to the behavioral round. Commonly, prior to
                starting the resume screening / interview process, the hiring
                managers would have aligned internally on the key criteria they
                are looking out for in the new candidate. For example, they
                could be looking for someone who is primarily more proactive as
                they want more independent drivers in the team. As such,
                interviewers will tend to look out for those specific traits and
                ask questions to sieve out those traits.
              </p>

              <p>However, some fail-safe, general tips to conduct yourself:</p>

              <ol class="numbered-list">
                <li>
                  <strong>Friendly and positive:</strong> Friendly and positive
                  people tend to fit well in any team.
                </li>
                <li>
                  <strong>Proactive and driven:</strong> Go-getters are
                  correlated with higher performance.
                </li>
                <li>
                  <strong>Enthusiastic:</strong> Demonstrate clear interest
                  about the role, team, and the company.
                </li>
              </ol>
