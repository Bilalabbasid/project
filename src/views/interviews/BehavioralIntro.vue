            <p>
              Here are the common evaluation categories and criteria from our
              experience conducting behavioral interviews for software engineers
              in big tech companies:
            </p>

            <div class="subsection">
              <h3>1. Collaboration</h3>
              <ul class="bullet-list">
                <li>Handling disagreements</li>
                <li>Working as a team</li>
                <li>Working with diverse personalities and skills</li>
                <li>Conveying complex ideas simply</li>
                <li>Giving constructive feedback</li>
                <li>Active listening</li>
              </ul>
            </div>

            <div class="subsection">
              <h3>2. Driving results and problem solving</h3>
              <ul class="bullet-list">
                <li>
                  <strong>Impact-driven mindset:</strong>
                  <ul class="nested-list">
                    <li>
                      Proactivity to make progress despite obstacles or
                      roadblocks
                    </li>
                    <li>Influencing others to deliver against objectives</li>
                    <li>Balancing analysis with decisive action</li>
                  </ul>
                </li>
                <li>
                  <strong
                    >Identifying best solutions and executing on them:</strong
                  >
                  <ul class="nested-list">
                    <li>Identifying the right problems</li>
                    <li>Identifying the most critical objectives</li>
                    <li>Resourcefulness and data-driven mindset</li>
                    <li>Creativity and innovation</li>
                    <li>Identifying tradeoffs and sustainable solutions</li>
                    <li>Measuring results, iterating and following through</li>
                  </ul>
                </li>
              </ul>
            </div>

            <div class="subsection">
              <h3>3. Growth mindset</h3>
              <ul class="bullet-list">
                <li>
                  Having self awareness of strengths and development areas
                </li>
                <li>
                  Actively soliciting feedback from others and creating feedback
                  loops
                </li>
                <li>
                  Taking feedback and setbacks well, viewing them as
                  opportunities
                </li>
                <li>Takes personal accountability for failures</li>
                <li>
                  Reflecting, modeling best practices and applying lessons to
                  new situations
                </li>
                <li>
                  Energized by setting and working towards challenging goals
                </li>
              </ul>
            </div>

            <div class="subsection">
              <h3>4. Adaptability and flexibility</h3>
              <ul class="bullet-list">
                <li>Performing well in uncertainty</li>
                <li>
                  Remaining calm and focused, serving as stable presence for
                  others
                </li>
                <li>Flexible and quick to adapt to unexpected situations</li>
              </ul>
            </div>
