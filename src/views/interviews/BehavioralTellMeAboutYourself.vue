            <li>Examples of good self introductions</li>
            <li>Tips and stories from hiring managers</li>
          </ol>

          <section>
            <h2>Recommended Preparation</h2>

            <p>
              In essence, you should always prepare a self introduction that is
              under 1 min in almost all cases.
            </p>

            <p>
              Prior to the interview, it is best to understand more about the
              evaluation criteria for the round from a recruiter or hiring
              manager — specifically if you will be evaluated on behavioral/fit
              aspects or purely on technical criteria. In big tech companies,
              coding rounds may totally exclude behavioral related criteria,
              hence your self introduction will most likely just serve as a
              "warm-up" to the main interview.
            </p>

            <p>
              However, in behavioral interviews or technical interviews in a
              startup or a company which prioritizes practical experience over
              LeetCode-style interviews, you may want to extend your self
              introduction to a quick portfolio showcase, or allude to the fact
              that you have a portfolio of your projects that they could view
              after the interview or allocate around 5 min at the end of the
              interview to run through.
            </p>

            <p>
              However, do note that in whichever case, if you are doing coding
              interviews with fixed format in the round, you should keep your
              self introduction short to ensure that you have more time on the
              actual coding test. Any extra time you take on your self
              introduction will be taken away from the time you have to answer
              technical questions.
            </p>

            <div class="table-section">
              <table>
                <thead>
                  <tr>
                    <th>Interview Type</th>
                    <th>Company Type</th>
                    <th>Recommended Length</th>
                    <th>What to Cover</th>
                  </tr>
                </thead>
                <tbody>
                  <tr>
                    <td>Coding or technical interviews</td>
                    <td>Big tech with fixed evaluation criteria</td>
                    <td>&lt; 1 min</td>
                    <td>1 min pitch</td>
                  </tr>
                  <tr>
                    <td>Coding or technical interviews</td>
                    <td>
                      Others which may emphasize less on standard LeetCode-style
                      coding tests
                    </td>
                    <td>~1 min</td>
                    <td>1 min pitch + allude to portfolio showcase</td>
                  </tr>
                  <tr>
                    <td>Behavioral or culture fit interviews</td>
                    <td>Any</td>
                    <td>~1 min</td>
                    <td>1 min pitch + allude to portfolio showcase</td>
                  </tr>
                </tbody>
              </table>
            </div>

            <p>
              To tackle all the possible cases, you should prepare a 1-min pitch
              and a project portfolio (optional but recommended).
            </p>
          </section>

          <section>
            <h2>The 1-min pitch</h2>

            <div class="subsection">
              <h3>The big picture</h3>

              <p>Most of the time, pitches will cover these 4 items:</p>

              <ol class="numbered-list">
                <li>Breadth and depth of job experience</li>
                <li>Top achievements</li>
                <li>
                  If less than 5 years of working experience, educational
                  background
                </li>
                <li>Motivation for applying for role</li>
              </ol>

              <p>
                When developing your pitch, keep the objective in mind — you
                want to leave a good first impression as a potential ideal
                candidate for the role. To do this, you should come off as
                confident and likable, on top of demonstrating key requirements
                of the role. Think about what you would look out for if you were
                to hire a candidate for this role.
              </p>

              <p>
                Do not attempt to go too deep — focus on covering the main
                points and enunciating them confidently and smoothly. As
                software engineers, we prove our mettle through projects and
                code. Talking too much too early may come off as over-eager and
                words alone don't hold much value.
              </p>

              <p>
                In essence, to hiring managers, the self introduction serves to
                answer these questions about the candidate for them:
              </p>

              <ol class="numbered-list">
                <li>
                  Is this candidate passionate about front end / software
                  engineering?
                </li>
                <li>Can they describe things in a clear and concise way?</li>
                <li>What kind of person are they? Confident or arrogant?</li>
                <li>
                  Would I want to work with this person? Do they seem coachable?
                </li>
              </ol>
            </div>

            <div class="subsection">
              <h3>Example pitch points for front end / web developers</h3>

              <p>
                On top of role-specific requirements such as team-specific
                frameworks and technologies, front end hiring managers typically
                focus on the following 4 criteria:
              </p>

              <div class="table-section">
                <table>
                  <thead>
                    <tr>
                      <th>Criteria</th>
                      <th>Example Pitch Points</th>
                    </tr>
                  </thead>
                  <tbody>
                    <tr>
                      <td>
                        Understanding of front end fundamentals: HTML, CSS,
                        JavaScript and related areas
                      </td>
                      <td>
                        "I have been building front end applications for a few
                        years now and also contributed to popular open source
                        projects like Lodash and jQuery." "I was a Teaching
                        Assistant for my college's web development course and
                        mentored students working on projects which involved
                        building full stack web applications."
                      </td>
                    </tr>
                    <tr>
                      <td>
                        Breadth and depth of front end technologies the
                        candidate knows
                      </td>
                      <td>
                        "I used React, Tailwind, Next.js, Prisma and MySQL to
                        build a Twitter clone as part of my software engineering
                        team project."
                      </td>
                    </tr>
                    <tr>
                      <td>
                        Initiative to keep up with modern front end technologies
                      </td>
                      <td>
                        "I learnt Astro and rebuilt my personal blog using it
                        because Astro is great for building content-driven
                        websites."
                      </td>
                    </tr>
                    <tr>
                      <td>
                        Relevant front end projects that the candidate has
                        worked on, complexity of those projects
                      </td>
                      <td>
                        "In my spare time I built a crypto price tracking app in
                        React to learn how to build data visualization-heavy
                        client apps and also solve a personal pain point of
                        tracking my portfolio."
                      </td>
                    </tr>
                  </tbody>
                </table>
              </div>

              <p>Refer below for examples of good self introductions.</p>
            </div>
          </section>

          <section>
            <h2>Portfolio showcase</h2>

            <p>
              If you were to extend into a showcase of your portfolio, you would
              want to ensure that your portfolio is minimal and easy to run
              through during the interview.
            </p>

            <p>The description for each project should encompass:</p>

            <ul class="bullet-list">
              <li>
                <strong>Purpose of project</strong> – What it does for users
              </li>
              <li><strong>Frameworks/technologies used</strong></li>
              <li><strong>Technical complexity involved</strong></li>
              <li><strong>Results, impact</strong> (optional)</li>
            </ul>
          </section>

          <section>
            <h2>Examples of good self introductions</h2>

            <div class="subsection">
              <h3>Experienced engineers</h3>

              <div class="example-section">
                <div class="sample-response">
                  <p>
                    "I worked at Airbnb as a Front End Engineer for 6 years and
                    was the lead developer on the project to redesign Airbnb's
                    booking experience. As part of my work, I contributed to
                    Airbnb's primary design system and was the person who build
                    several UI components like the image carousel, ratings
                    widget, and improving on existing components. I also had
                    experience contributing to Airbnb's open source front end
                    projects like Enzyme and react-dates.
                  </p>
                  <p>
                    In my spare time, I enjoy learning new front end
                    technologies and catching up with the ever-evolving front
                    end ecosystem. Most recently I've been exploring new
                    JavaScript frameworks like Astro and SolidJS to expose
                    myself to new ways of building user interfaces and improve
                    my skills as a Front End Engineer."
                  </p>
                </div>
              </div>
            </div>

            <div class="subsection">
              <h3>Fresh graduates / College students</h3>

              <div class="example-section">
                <div class="sample-response">
                  <p>
                    "I graduated from Northeastern University in 2020 with a
                    degree in Computer Science. My interests are in Front End
                    development and I enjoy building delightful products on the
                    web because of the freedom to develop whatever comes to mind
                    and the fast feedback loop.
                  </p>
                  <p>
                    I took a web development course while in school which taught
                    me the fundamentals of full stack development. With these
                    skills, I managed to secure software engineering internships
                    at companies like Uber and Palantir. At Uber, I built an
                    internal tool to help developers manage their test rider and
                    driver accounts using React, Redux, and GraphQL.
                  </p>
                  <p>
                    I'm interested in the Front End Engineer role at Google
                    because I use Google products on a daily basis and am very
                    impressed by their well thought out product experience. It'd
                    be a dream come true to work on such products that impact
                    the lives of billions."
                  </p>
                </div>
              </div>
