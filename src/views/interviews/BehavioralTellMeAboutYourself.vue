          <h1>Answering "Tell Me About Yourself"</h1>
          <p class="article-subtitle">
            How to craft a compelling response to the most common behavioral
            interview opener
          </p>
        </header>
        <article class="article-content">
          <p class="lead-paragraph">
            The self introduction is one of the things you can always expect to
            encounter in an interview, regardless of interview format. Hence the
            return on investment of preparing for it is rather high. Most of the
            time, your self introduction should be short and sweet, delivering a
            good impression in the fewest amount of words.
          </p>

          <p>In this article you will find:</p>

          <ol class="numbered-list">
            <li>Recommended preparation for different interview settings</li>
            <li>The 1-min pitch formula</li>
