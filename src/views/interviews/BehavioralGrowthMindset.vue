          <section>
            <h2>Sample questions and answers</h2>

            <div class="subsection">
              <h3>
                How do you stay up to date on the latest front end technologies
                and techniques?
              </h3>
              <div class="example-box">
                <p>
                  "I have been working at a big tech company for the past two
                  years now and the company uses a mix of external and internal
                  technologies for our front end stack. Because we have a huge
                  code base and established practice of doing things, we tend to
                  not upgrade libraries that often and change our technology
                  choices. It was only after a year into the job that I realized
                  that I have been too comfortable in my role since I have fully
                  ramped up on the code base and haven't learnt anything new for
                  the past year. It was then that I decided that I have to be
                  proactive in my learning in order to keep my skills sharp and
                  relevant.
                </p>

                <p>
                  The Front End ecosystem moves really fast and there's a joke
                  that there's a new JavaScript library emerging every day.
                  Thankfully the ecosystem is more stable compared to 2015/2016
                  when a new wave of front end tooling first emerged. To keep
                  myself updated without suffering from JavaScript fatigue, I
                  spend a weekend every month going through front end
                  newsletters like "This Week in React" and "JavaScript Weekly"
                  and reading engineering blogs. If I see something interesting
                  or is relevant to my job, I will dive deeper into them by
                  trying out small examples and building small prototypes in my
                  company's code base. For example, I recently discovered React
                  Query, which is a data fetching library for React that uses a
                  declarative paradigm for fetching data and am trying to
                  incorporate it into my work. I also recently discovered tRPC,
                  a library that enforces type safety between client and server
                  boundaries, which is an issue that has caused some bugs for
                  us.
                </p>

                <p>
                  To make this learning process more fun and collaborative, I
                  started a front end social club within my company by creating
                  a new Slack channel and inviting a few like-minded coworkers
                  to join. Anyone can share front end news they find interesting
                  there and discuss front end technologies. It has been pretty
                  well-received, with over 30 people currently in the channel
                  and activity nearly everyday. A side effect of this initiative
                  is that people also start asking questions about front end
                  issues they are facing at work, and we created another Slack
                  channel for people to get help on front end issues. Feedback
                  has been really positive so far!"
                </p>
              </div>

              <p>
                This answer uses the STAR technique in a subtle fashion and
                demonstrates many qualities interviewers would like to see in
                candidates:
              </p>

              <ul class="bullet-list">
                <li>Passion: Keen to learn more about front end domain</li>
                <li>
                  Growth: Identified that the lack of growth would be a problem
                </li>
                <li>Initiative: Acted on the problem of lack of growth</li>
                <li>
                  Problem solving: Thought of ways to solve the lack of growth,
                  and executed on it
                </li>
                <li>
                  Building camaraderie: Rallying like minded folks for a common
                  cause
                </li>
                <li>
                  Helps improves others: Created Slack channels for sharing
                  knowledge and supporting others
                </li>
              </ul>
            </div>

            <div class="subsection">
              <h3>
                Can you provide an example of a time when you had to adapt to a
                new technical environment or framework?
              </h3>
              <div class="example-box">
                <p>
                  "When I joined my current company back in 2019, I was fresh
                  out of college and had no experience with the front end stack
                  that the company was using, which used React, Apollo, Styled
                  Components, TypeScript for their front end stack and a Django
                  + GraphQL back end. Back in college, I was mainly using Vue.js
                  for front end development as it was the JavaScript framework
                  of choice taught in the web development class and haven't used
                  GraphQL before.
                </p>

                <p>
                  It was definitely a huge learning curve for me at the start
                  and I was really afraid of underperforming due to my
                  unfamiliarity with the tech stack. Thankfully I had a mentor
                  who gave me a lot of guidance on the tech stack and came up
                  with a number of onboarding tasks which were progressively
                  harder. I first spent a few days reading the documentation
                  websites of the technologies and trying out the examples on
                  their websites. I made it a point to understand the problems
                  that these libraries were solving and how they were better
                  than prior art because I think knowing that is important for
                  fully appreciating the library and using the right tool for
                  the right job. I also looked up some resources that compared
                  Vue.js to React, as they were quite similar yet had some
                  differences. That helped me to learn React faster (fully
                  grasping React hooks still took me some time though). When I
                  had time at nights or on weekends, I'd explore building small
                  projects using these new technologies and also rebuild my
                  personal blog using Gatsby because it used both React and
                  GraphQL.
                </p>

                <p>
                  Within two months, I felt like I had learnt so much and was
                  comfortable with most of the code base. I could build full
                  stack features without much guidance from my mentor. To help
                  future new employees who might face the same onboarding
                  challenge, I jotted down my learnings in our internal wiki
                  along with links to the best resources for learning the topic.
                  My manager appreciated the initiative and commended me for
                  that. Today, it is part of our official engineering onboarding
                  resource and I update it every once in a while. A few new
                  joiners have also thanked me for sharing my knowledge in the
                  wiki and making their onboarding process smoother."
                </p>
              </div>

              <p>Analysis of the answer:</p>

              <ul class="bullet-list">
                <li>Coachable: Follows guidance/suggestions by the mentor</li>
                <li>
                  Problem solving: Came up with a few ways to help themselves
                  onboard faster
                </li>
                <li>Initiative: Initiative to start the wiki</li>
                <li>
                  Empathy: See that others might face the same issues and
                  started the wiki
                </li>
                <li>
                  Helps improves others: New joiners benefit from the wiki
                </li>
              </ul>
