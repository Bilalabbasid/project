          <h1>Growth Mindset Questions</h1>
          <p class="article-subtitle">
            How to answer questions about learning, failure, and continuous
            improvement
          </p>
        </header>
        <article class="article-content">
          <p class="lead-paragraph">
            As mentioned in our behavioral interview preparation overview,
            growth mindset is one of the 8 main categories of questions to
            prepare for. In this guide, you will learn how to tackle them:
            evaluation criteria in detail, abstracting possible questions into
            common themes, suggested answer framework, and sample questions and
            answers.
          </p>

          <section>
            <h2>Evaluation criteria in detail</h2>

            <p>
              When evaluating candidates under this category, interviewers are
              often looking at the following criteria:
            </p>

            <ul class="bullet-list">
              <li>Having self awareness of strengths and development areas</li>
              <li>
                Actively soliciting feedback from others and creating feedback
                loops
              </li>
              <li>
                Taking feedback and setbacks well, viewing them as opportunities
              </li>
              <li>Takes personal accountability for failures</li>
              <li>
                Reflecting, modeling best practices and applying lessons to new
                situations
              </li>
              <li>
                Energized by setting and working towards challenging goals
              </li>
              <li>Proactively learning new skills and applying to work</li>
            </ul>
          </section>

          <section>
            <h2>Abstracting growth mindset questions</h2>

            <div class="subsection">
              <h3>Common questions</h3>
              <ul class="bullet-list">
                <li>
                  What do you think is your greatest area for improvement?
                </li>
                <li>
                  Can you tell me about a time you improved upon your work upon
                  receiving criticism or feedback?
                </li>
                <li>
                  Tell me about a time you set a difficult goal for yourself and
                  achieved it
                </li>
                <li>
                  Tell me about a time you learnt a new skill outside of work
                  and applied it to your work
                </li>
                <li>
                  Tell me about a time you learnt a new skill set after
                  observing others leveraging it to great success
                </li>
                <li>
                  How do you stay up to date on the latest front end
                  technologies and techniques?
                </li>
                <li>
                  Can you provide an example of a time when you had to adapt to
                  a new technical environment or framework?
                </li>
                <li>
                  Imagine you need to implement a new system that you do not
                  have much experience with. What steps do you take to gather
                  information and ensure successful execution?
                </li>
              </ul>
            </div>

            <div class="subsection">
              <h3>Themes identified</h3>
              <p>
                As mentioned in our behavioral interview preparation overview,
                it is impractical to prepare answers specifically for every
                behavioral question out there. However, by batching specific
                questions into similar themes and preparing stories that cover a
                large number of question requirements, we can reduce the number
                of stories to prepare to around 3-5 stories.
              </p>

              <p>These are the themes which seem to recur:</p>

              <ul class="bullet-list">
                <li>Receiving and managing feedback</li>
                <li>Mindset towards challenges and stretch goals</li>
                <li>Continued learning</li>
              </ul>
            </div>
          </section>

          <section>
            <h2>Suggested answer framework</h2>

            <p>
              As always, the STAR format is the simplest and most effective
              framework that we recommend to structure your story.
            </p>

            <p>
              Here are our recommended approaches for each identified theme,
              with which you can structure your story around:
            </p>

            <div class="subsection">
              <h4>Receiving and managing feedback/setbacks</h4>
              <ol class="numbered-list">
                <li>
                  Actively solicit feedback from others where possible e.g.
                  after every large project
                </li>
                <li>
                  Take personal accountability for mistakes and produce an
                  action plan to address them
                </li>
                <li>
                  Have a healthy mindset towards feedback / setbacks
                  <ol class="nested-list">
                    <li>View them as opportunities to improve yourself</li>
                    <li>
                      Focus on being constructive and solutions-focused than
                      emotions-driven
                    </li>
                  </ol>
                </li>
                <li>
                  Take steps to reflect upon past experiences in order to model
                  best practices and put them into action in the next relevant
                  situation
                </li>
              </ol>
            </div>

            <div class="subsection">
              <h4>Mindset towards challenges / stretch goals</h4>
              <ol class="numbered-list">
                <li>
                  Derive energy from setting and working towards stretch goals,
                  seeing them as opportunities for growth
                </li>
                <li>
                  Display consistency and discipline in following through with
                  detailed plan
                </li>
              </ol>
            </div>

            <div class="subsection">
              <h4>Continued learning</h4>
              <p>
                A good answer combines a consistent schedule and effort placed
                into learning, as well as how the learning was proactively
                applied to work. e.g. "I learned about X because I was doing Y.
                This impacted the way that I was tackling Z at work because it
                made me realize W and V".
              </p>

              <p>
                The points below are key ideas you can structure your answer
                around technical learning; however, you should still add on
                concrete examples of events, newsletters or communities you have
                actually participated in, to showcase your willingness to keep
                up with trends.
              </p>

              <ol class="numbered-list">
                <li>
                  Follow relevant blogs, newsletters, podcasts that cover the
                  latest developments in front end technologies and techniques.
                  Give some examples (which you do actually follow) and the
                  content or tech they provide.
                  <ol class="nested-list">
                    <li>
                      <strong>Front End Influencers</strong>
                      <ul class="nested-list">
                        <li>
                          Dan Abramov — Co-creator of Redux, React core team
                        </li>
                        <li>Evan You — Creator of Vue.js and Vite</li>
                        <li>Rich Harris — Creator of Svelte and Rollup</li>
                        <li>
                          Kent C. Dodds — Popular React ecosystem course creator
                        </li>
                        <li>
                          Josh W. Comeau — Popular front end course creator
                        </li>
                      </ul>
                    </li>
                    <li>
                      <strong>Blogs & Magazines</strong>
                      <ul class="nested-list">
                        <li>CSS-Tricks</li>
                        <li>ui.dev</li>
                        <li>JavaScript Weekly</li>
                        <li>Best of JavaScript</li>
                      </ul>
                    </li>
                    <li>
                      <strong>Podcasts</strong>
                      <ul class="nested-list">
