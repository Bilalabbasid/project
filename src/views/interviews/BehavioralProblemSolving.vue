            <li>Possible nature of follow-up questions</li>
            <li>Sample questions and answers</li>
          </ol>

          <section>
            <h2>Evaluation criteria in detail</h2>

            <p>
              Problem solving and drive for results are grouped together into 1
              category as they are highly related in nature. When describing
              one's experience in solving problems or to achieve certain results
              / objectives, their mindset or drive to do so can also be
              inferred.
            </p>

            <p>
              When rating candidates under this category, interviewers are often
              looking at the following criteria:
            </p>

            <ul class="bullet-list">
              <li>
                <strong
                  >Identifying best solutions and executing on them</strong
                >
                <ul class="bullet-list">
                  <li>Identifying the right problems</li>
                  <li>Identifying the most critical objectives</li>
                  <li>Resourcefulness and data-driven mindset</li>
                  <li>Creativity and innovation</li>
                  <li>Identifying tradeoffs and sustainable solutions</li>
                  <li>Measuring results, iterating and following through</li>
                </ul>
              </li>
              <li>
                <strong>Impact-driven mindset</strong>
                <ul class="bullet-list">
                  <li>
                    Proactivity to make progress despite obstacles or roadblocks
                  </li>
                  <li>Influencing others to deliver against objectives</li>
                  <li>Balancing analysis with decisive action</li>
                </ul>
              </li>
            </ul>
          </section>

          <section>
            <h2>Abstracting possible questions into common themes</h2>

            <p>
              As mentioned in our behavioral interview preparation overview, it
              is impractical to prepare answers specifically for every
              behavioral question out there. However, by batching specific
              questions into similar themes and preparing stories that cover a
              large number of question requirements, we can reduce the number of
              stories to prepare to around 3-5 stories.
            </p>

            <p>
              There's simply too many types of problem solving and drive for
              results behavioral questions that could be asked, such as:
            </p>

            <ul class="bullet-list">
              <li>
                "Can you tell me about a time when you had to use data to drive
                engineering decisions?"
              </li>
              <li>
                "Can you provide an example of a time when you had to
                troubleshoot and fix a complex issue in a project?"
              </li>
              <li>
                "Can you describe a time you creatively solved an engineering
                problem or achieved meaningful metric improvement?"
              </li>
              <li>
                "Can you describe a time when you had to make an important
                engineering decision and how you decided between tradeoffs?"
              </li>
            </ul>

            <p>
              However, when we look at 80% of questions under this category,
              questions typically ask for how a problem was solved or a specific
              trait required for effective problem solving, such as creativity,
              using data or trade-off evaluation. The source of the problem or
              objective, as well as whether the candidate was resilient in the
              face of obstacles can be inferred from answers to these questions
              as well.
            </p>
          </section>

          <section>
            <h2>Suggested answer framework</h2>

            <p>
              As always, the STAR format is the simplest and most effective
              framework that we recommend to structure your story.
            </p>

            <p>
              Despite the large number of question possibilities, we can
              abstract all the requirements for problem solving behavioral
              questions by thinking about a robust problem solving process:
            </p>

            <ol class="numbered-list">
              <li>
                <strong>Problem identification:</strong> Identifying the correct
                root cause or root problem to focus on from surface level
                symptoms
              </li>
              <li>
                <strong>Metrics/target setting:</strong> Identify key metrics
                that would signify success in solving the problem (if any)
              </li>
              <li>
                <strong>Information gathering:</strong> Resourcefulness in
                gathering information from different sources and a data-driven
                mindset (using data to drive decisions)
              </li>
              <li>
                <strong>Solution brainstorming:</strong> Creatively generate
                solutions that solve the root cause
              </li>
              <li>
                <strong>Solution evaluation:</strong> Evaluate trade-offs of
                each solution and select the most optimal one
              </li>
              <li>
                <strong>Monitoring and adjustment:</strong> Monitor
                effectiveness of the solution by measuring key metrics. Adjust
                strategy if needed
              </li>
            </ol>

            <p>
              Hence, you just have to ensure that at least 1 of your prepared
              stories or projects covers all of the above steps. In doing so,
              the story could be reused for all affiliated traits under problem
              solving, such as data-driven mindset, resourcefulness, creativity,
              and working with feedback from users.
            </p>

            <p>
              Of course, you might need to tailor the details of your story to
              focus on the specific aspect asked in the specific question.
            </p>

            <p>
              We recommend you to also pick problem solving stories that can
              doubly serve to get signals on the following:
            </p>

            <ul class="bullet-list">
              <li>
                <strong>Proactivity/initiative-taking:</strong> You had taken
                the initiative to look into the problem, gather information and
                solve it
              </li>
              <li>
                <strong>Leadership:</strong> You had led the problem solving
                process
              </li>
              <li>
                <strong>Teamwork:</strong> You had to work as part of a team to
                solve the problem
              </li>
            </ul>
          </section>

          <section>
            <h2>Sample story</h2>

            <p>Here's a sample story using the STAR format.</p>

            <div class="subsection">
              <h3>Situation</h3>
              <p>
                I was the tech lead for an e-commerce website selling luxury
                goods. The website was built as an Angular 1.5 single-page
                application. In recent years, the product was showing its age –
                developer experience was not great and the website performance
                was poor. Initial loading speed was over 3 seconds and the
                conversion rate was around 0.8%.
              </p>
            </div>

            <div class="subsection">
              <h3>Task</h3>
              <p>
                I was tasked with improving the performance and conversion of
                the website.
              </p>
            </div>

            <div class="subsection">
              <h3>Actions</h3>

              <div class="principle-section">
                <h4>1. Problem identification</h4>
                <ul class="bullet-list">
                  <li>Conversion is tied to website performance and UX</li>
                  <li>
                    Website performance has been on a gradual decline over the
                    past few years as there wasn't a person to oversee the
                    overall architecture of the website and track performance
                    metrics
                  </li>
                  <li>UX hasn't been looked at in a while</li>
                </ul>
              </div>

              <div class="principle-section">
                <h4>2. Information gathering</h4>
                <ul class="bullet-list">
                  <li>
                    Looked at nature of bugs in the past year, categorized them
                    according to their root causes to identify hotspots and
                    major problematic areas.
                  </li>
                  <li>
                    Gathered feedback from team regarding areas of improvements.
                  </li>
                  <li>
                    Together with the product manager, ran a brainstorming
                    session with the team to think of ways to improve.
                  </li>
                  <li>
                    In order to improve, firstly we need to know how we are
                    doing:
                    <ul class="bullet-list">
                      <li>
                        Double checked that our performance and conversion
                        tracking was working correctly.
                      </li>
                      <li>
                        Started tracking new metrics from Lighthouse and Core
                        Web Vitals.
                      </li>
                    </ul>
                  </li>
                  <li>
                    Worked with Data Scientists to come up with dashboards for
                    performance and conversion and gained some insights:
                    <ul class="bullet-list">
                      <li>
                        Identified that some countries had lower conversion
                        rates.
                      </li>
                      <li>
                        Mobile users had lower conversion rates as compared to
                        desktop.
                      </li>
                    </ul>
                  </li>
                  <li>
                    Worked with UX Designers and UX Researchers to identify
                    problems in the end-to-end shopping experience on the
                    website.
                    <ul class="bullet-list">
                      <li>
                        UI elements were too spaced apart and required a lot of
                        scrolling which affected bounce rate because some users
                        didn't bother scrolling.
                      </li>
                    </ul>
                  </li>
                </ul>
              </div>

              <div class="principle-section">
                <h4>3. Solution brainstorming</h4>
                <ul class="bullet-list">
                  <li>
                    <strong>User interface:</strong> Server side rendering is
                    crucial for its performance and SEO benefits.
                    <ul class="bullet-list">
                      <li>
                        Angular.js 1.6 to Angular 17 was a huge undertaking and
                        there was no significant time savings by staying on
                        Angular since Angular 17 is a completely different
                        framework.
                      </li>
                      <li>
                        Next.js: A few of our developers have experience with
                        React and Next.js as a metaframework for building SSR
                        (server-side rendering) applications was rapidly rising
                        in popularity. We really desired the fast initial load
                        and app-like navigation behavior that Next.js provided.
                      </li>
                      <li>
                        Svelte: Reactive model was appealing and the programming
                        model easier to understand as compared to React's,
                        however the ecosystem is smaller than React and there
                        aren't as many examples of successful e-commerce
                        websites built in Svelte.
                      </li>
                    </ul>
                  </li>
                  <li>
                    <strong>Styling:</strong> The stylesheet was getting very
                    bloated due to many classes being added over the years and
                    being hard to remove.
                    <ul class="bullet-list">
                      <li>
                        Tailwind CSS: Tailwind CSS was among the hottest CSS
                        methodologies and its atomic CSS approach scales well
                        for growing code bases.
                      </li>
                      <li>
                        Styled Components: CSS-in-JS was something we were also
                        looking at, but Styled Components was tied to React and
                        runtime style injection was bad for performance.
                      </li>
                    </ul>
                  </li>
                  <li>
                    <strong>Performance-centric mindset:</strong> Read many
                    performance case studies on web.dev and engineering blogs of
                    other e-commerce companies, gathered a list of important
                    performance techniques and processes:
                    <ul class="bullet-list">
                      <li>
                        Set performance budget for each page (under 300kb)
                      </li>
                      <li>
                        Run performance benchmarks as part of CI – before
                        merging Pull Requests and continuously
                      </li>
                      <li>Lazy loading of non-critical components</li>
                      <li>Lazy load below-the-fold contents</li>
                      <li>
                        Split JavaScript on page level instead of a single
                        bundle (handled by Next.js)
                      </li>
                      <li>Use WebP format for images</li>
                      <li>Host images on a CDN instead</li>
                      <li>
                        Adaptive loading of images so that mobile devices load a
                        smaller image
                      </li>
                      <li>
                        Consolidated duplicate JavaScript libraries (data-fns
                        and moment.js), switched to `lodash-es`, removed all
                        usages of jQuery
                      </li>
                      <li>
                        Looked at data to identify lesser-used features and
                        removed them from the code, reducing JS size on the
                        product details pages by 200+kb
                      </li>
                    </ul>
                  </li>
                  <li>
                    <strong>Search Engine Optimization (SEO)</strong>
                    <ul class="bullet-list">
                      <li>
                        Used SEO tools like Ahrefs to continuously monitor SEO
                      </li>
                      <li>
                        Worked with the marketing team to ensure marketing copy
                        included important keywords as shown by Ahrefs
                      </li>
                      <li>Adjusted page URLs to include SEO keywords</li>
                    </ul>
                  </li>
                  <li>
                    <strong>User experience improvements</strong>
                    <ul class="bullet-list">
                      <li>
                        Single page checkout experience as opposed to two-page
                        checkout to reduce clicking
                      </li>
                      <li>Reduced height of many UI elements</li>
                      <li>Fixed checkout button that wouldn't be missed</li>
                    </ul>
                  </li>
                  <li>
                    <strong>Payment improvements</strong>
                    <ul class="bullet-list">
                      <li>
                        Analyzed Stripe checkout data and implemented
                        country-specific address fields
                      </li>
                      <li>
                        Initially only had one available payment method – credit
                        card. Enlisted the help of data scientists to evaluate
                        the popularity of new payment methods and whether they
                        were worth adding. We later added PayPal, Google Pay,
                        and Apple Pay payment methods as well
                      </li>
                    </ul>
                  </li>
                </ul>
              </div>

              <div class="principle-section">
                <h4>4. Solution evaluation</h4>
                <ul class="bullet-list">
                  <li>
                    <strong>Metaframework:</strong> Chose Next.js because it is
                    backed by a company, there were many available resources,
                    and has the largest community of all. React is also the most
                    popular JavaScript library out there and also easiest to
                    hire for
                  </li>
                  <li>
                    <strong>Styling:</strong> Tailwind because it's a reliable
                    and futureproof option (compatible with the direction React
                    was heading)
                  </li>
                </ul>
              </div>

              <div class="principle-section">
                <h4>5. Monitoring and adjustment</h4>
                <ul class="bullet-list">
                  <li>
                    Rolled out the new website behind an A/B test while
                    monitoring the performance and conversion rates over a
                    period of 2 months.
                  </li>
                  <li>
                    Countries which previously saw lower conversion rates
                    experienced nearly 50% improvements in conversion rates.
                  </li>
                </ul>
              </div>
            </div>

            <div class="subsection">
              <h3>Results</h3>
              <ul class="bullet-list">
                <li>Lighthouse score improved to 92</li>
                <li>Loading speed improved to 1.5 seconds</li>
                <li>Conversion improved to 2.5%</li>
                <li>
                  Developer velocity has improved in the recent survey and it is
                  now easier to hire people into the team because more people
                  know React over other frameworks
                </li>
              </ul>
            </div>

            <p>
              Obviously the entire story is too detailed as a response for a
              single question; you definitely shouldn't be listing all the
              specific performance improvements you made, like a laundry list!
              Aim to achieve both breadth and depth by briefly covering each
              phase within the "Action" section and going deep into the parts
              that showcase the signals required for that specific scenario.
            </p>
          </section>

          <section>
            <h2>Possible nature of follow-up questions</h2>

            <p>
              As mentioned in our behavioral interview preparation overview,
              interviewers are encouraged to rely more on follow-up questions to
              really understand the candidate's thought process and motivations,
              which typically fall under these categories:
            </p>

            <ul class="bullet-list">
              <li>
                <strong>Why do you think you did (insert action)?</strong>
              </li>
              <li><strong>Why did you not do (insert action)?</strong></li>
              <li>
                <strong
                  >How would you do things differently in hindsight?</strong
                >
              </li>
            </ul>

            <p>
              For questions on problem solving or drive for results,
              interviewers will most likely probe for questions to help them
              understand a bit more about:
            </p>

            <ul class="bullet-list">
              <li>
                <strong
                  >The source of the task/problem/objective (Understanding level
                  of proactivity and initiative):</strong
                >
                <ul class="bullet-list">
                  <li>
                    Was the project or task initiated by you? And to what
                    extent?
                  </li>
                  <li>
                    Was the underlying idea from you, or just the plan to
                    execute?
                  </li>
                  <li>
                    How did you get buy-in from stakeholders to even work on it?
                  </li>
                </ul>
              </li>
              <li>
                <strong>Role and actual contribution by candidate:</strong>
                <ul class="bullet-list">
                  <li>
                    Was there a team involved with solving the problem or
                    achieving the objective?
                  </li>
                  <li>
                    Which actions were initiated on your own accord or
                    contributed solely by you, and what did others do?
                  </li>
                  <li>
                    How did you help the other members involved to achieve the
                    goal?
                  </li>
                </ul>
              </li>
              <li>
                <strong
                  >Prioritizing the problem or objective to achieve:</strong
                >
                <ul class="bullet-list">
                  <li>
                    Was there a reason why this particular problem or objective
                    was prioritized to be addressed or done against others?
                  </li>
                  <li>
                    Was there another problem or root objective that should have
                    been more important?
                  </li>
                  <li>
                    Was this problem not already resolved by another team in the
                    company? Why did the solution have to be reinvented?
                  </li>
                </ul>
              </li>
              <li>
                <strong
                  >Selection of appropriate metrics/objectives, and if they were
                  measured post-launch:</strong
                >
                <ul class="bullet-list">
                  <li>
                    Was there any quantitative or qualitative target set, and
                    how were they decided?
                  </li>
                  <li>
                    How were they measured post-launch, and what was the result?
                  </li>
                  <li>
                    If an issue was resolved, what measures were put in place to
                    ensure the same problem didn't happen again?
                  </li>
                </ul>
              </li>
              <li>
                <strong
                  >Using sufficient information to drive decisions:</strong
                >
                <ul class="bullet-list">
                  <li>
                    What types of research or data did you rely on to make
                    decisions / choose a problem to resolve?
                  </li>
                  <li>
                    How much time did you spend on research and gathering
                    information?
                  </li>
                  <li>
                    How did you balance between gathering information / planning
                    and actually executing on the plan?
                  </li>
                </ul>
              </li>
              <li>
                <strong>Choosing the right solution:</strong>
                <ul class="bullet-list">
                  <li>
                    What other solutions were considered, and what were the pros
                    and cons of each?
                  </li>
                  <li>Why was the final solution chosen?</li>
                  <li>
                    Who came up with the original idea of the solutions? How
                    were they derived?
                  </li>
                </ul>
              </li>
            </ul>
          </section>

