              interview disposition to increase your chances of setting a good
              impression.
            </p>

            <div class="subsection">
              <h3>
                How do you ensure that your message is understood and
                well-received by your audience?
              </h3>
              <p>
                You could answer the question with a slight modification of the
                sample story, focused on the explanation of the technical
                information:
              </p>
              <div class="example-box">
                <p>
                  "To ensure that my message is well understood, after
                  explaining, I will provide a few examples to illustrate my
                  points and also ask my audience a few questions to confirm
                  their understanding. In a project I did a few months ago, I
                  had to explain to the business team why and how an urgent
                  feature had been delayed by external team API delays. To do
                  so, I focused on the key takeaways that business needed —
                  which was how the feature was impacted by such a delay, and
                  utilized visual aids as well as used simple and clear
                  language. I also gave examples while asking questions to
                  ascertain their understanding of my explanation. This allowed
                  the business team to understand the dependency well enough to
                  brainstorm ways to deliver the feature even without the
                  dependencies."
                </p>
              </div>
            </div>

            <div class="subsection">
              <h3>
                How do you approach working with teammates who are not meeting
                their deadlines or responsibilities?
              </h3>
              <div class="example-box">
                <p>
                  "Once it is clear that their behaviors may constitute a
                  concerning pattern, I would let them know in a timely manner.
                  For instance, I had a teammate on a project who had missed bug
                  fix timelines regularly, and once it happened a number of
                  times, I scheduled a private meeting with him to talk to them
                  about it. I focused on framing the conversation as a growth
                  opportunity for him on top of what is already appreciated
                  about them, which was his knowledge and mentorship as a senior
                  developer. Instead of describing a general behavior such as
                  "being late", I mentioned the specific tickets that were
                  missed and the impact that had occurred from missing them, as
                  well as its effect on the morale of the team. I then offered a
                  few solutions, such as enacting a new reminder bot within the
                  channel for tickets that were due soon. He was able to
                  understand where I was coming from and our team was able to
                  meet our deadlines much better after that."
                </p>
              </div>
