            <h2>Possible nature of follow-up questions</h2>

            <p>
              As mentioned in our behavioral interview preparation overview,
              interviewers are encouraged to rely more on follow-up questions to
              really understand the candidate's thought process and motivations,
              which typically fall under these categories:
            </p>

            <ul class="bullet-list">
              <li>Why do you think you did (insert action)?</li>
              <li>Why did you not do (insert action)?</li>
              <li>How would you do things differently in hindsight?</li>
            </ul>

            <p>
              For questions on collaboration, interviewers will most likely
              probe for questions to help them understand a bit more about:
            </p>

            <ul class="bullet-list">
              <li>
                Understanding if and why certain stakeholders were involved or
                not involved
                <ul class="nested-list">
                  <li>
                    Which stakeholders were involved in the discussion, and why?
                  </li>
                  <li>
                    Why were certain stakeholders like (insert stakeholder)
                    excluded from the group?
                  </li>
                </ul>
              </li>
              <li>
                Understanding group dynamics and how it affected the strategy to
                handle the group
                <ul class="nested-list">
                  <li>
                    Which personalities were present in the group and how did it
                    affect dynamics?
                  </li>
                  <li>
                    Did this understanding affect your strategy to navigate
                    group discussions?
                  </li>
                </ul>
              </li>
              <li>
                Understanding the candidate's mindset to conflict
                <ul class="nested-list">
                  <li>
                    How do you think conflicts like this affect the working
                    environment? Would you prefer to avoid them?
                  </li>
                </ul>
              </li>
              <li>
                Understanding how the resolution was derived; did the candidate
                merely defer to others as opposed to relying on facts and data?
                <ul class="nested-list">
                  <li>
                    Were there any data and information which were leveraged in
                    making the decision?
                  </li>
                  <li>
                    Was there an analysis of each solution's pros and cons?
                  </li>
                </ul>
              </li>
            </ul>
          </section>

