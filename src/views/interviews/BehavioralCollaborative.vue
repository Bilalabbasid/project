          <section>
            <h2>Suggested answer framework</h2>

            <p>
              As always, the STAR format is the simplest and most effective
              framework that we recommend to structure your story.
            </p>

            <p>
              Based on the abstraction above, we can see that interviewers are
              looking for specific traits for every sub-requirement. We have
              created a quick cheatsheet for suggested approaches to exhibit
              these desired traits.
            </p>

            <p>
              Ideally, you should select stories that can cover as much of the
              below traits as possible, so that you don't have to remember too
              many stories.
            </p>

            <div class="subsection">
              <h4>Convey complex ideas simply</h4>
              <ol class="numbered-list">
                <li>
                  First understand your audience's level of knowledge. Put
                  yourself in their shoes, explain in their language
                </li>
                <li>
                  Understand what they need to know (and want to know) and
                  prioritize the key takeaways
                </li>
                <li>
                  Only dive deeper where appropriate for important details
                </li>
                <li>Break down the idea into component parts</li>
                <li>Use visual aids, such as diagrams or charts</li>
                <li>Give examples</li>
              </ol>
            </div>

            <div class="subsection">
              <h4>Active listening</h4>
              <ol class="numbered-list">
                <li>Focus on understanding rather than replying</li>
                <li>Withhold judgment until the person is done talking</li>
                <li>Use non-verbal cues to show that you are engaged</li>
                <li>Summarize and repeat back in your own words</li>
                <li>Ask clarifying questions</li>
                <li>Never interrupt</li>
              </ol>
            </div>

            <div class="subsection">
              <h4>Working as a team</h4>
              <ol class="numbered-list">
                <li>
                  Set clear goals and ensure that everyone understands them.
                  Proactively seek alignment and perspectives
                </li>
                <li>
                  Assign and communicate roles and responsibilities. Collaborate
                  and delegate
                </li>
                <li>
                  Set up regular progress check-ins and address blockers. Always
                  include the appropriate stakeholders and share timely
                  information with them
                </li>
                <li>Measure impact and recognize achievements</li>
              </ol>
            </div>

            <div class="subsection">
              <h4>
                Working with diverse working styles, skills, and personalities
              </h4>
              <ol class="numbered-list">
                <li>
                  Openly recognize unique perspectives and skills that each team
                  member can bring to the table
                </li>
                <li>
                  Encourage open and honest communication, and active listening
                </li>
                <li>
                  Create a welcoming and supportive environment even to
                  differences in opinions. Use them as opportunities to grow
                </li>
                <li>
                  Proactively include a range of perspectives and voices in
                  decision-making
                </li>
                <li>
                  Provide equal opportunities to every team member, including
                  access to channels, resources and support
                </li>
              </ol>
            </div>

            <div class="subsection">
              <h4>Handling disagreements</h4>
              <ol class="numbered-list">
                <li>
                  Facilitate open and productive communication between relevant
                  parties
                </li>
                <li>
                  Frame conflict as a way to enhance teamwork and improve
                  current solutions
                </li>
                <li>
                  Clarify the source of conflict (and whether there actually is
                  conflict)
                </li>
                <li>
                  Give each party equal time to air out their views and concerns
                  without judgment (assume positive intent). Set ground rules if
                  needed to cultivate active listening and understanding. Pivot
                  conversation away from emotions and towards solutions.
                  Tactfully address unproductive dialogue
                </li>
                <li>
                  Summarize and validate the standpoint from each party,
                  reflecting back at them
                </li>
                <li>
                  Identifying underlying sources of conflict between the
                  standpoints
                </li>
                <li>
                  Brainstorm and run through the options available to best meet
                  common goals. (Show skill and logic in finding common ground).
                  Use data and facts to drive resolution with others, weighing
                  pros and cons, instead of just relying on opinions
                </li>
                <li>
                  Agree on the best solution and determine each party's
                  responsibilities. Bring in relevant parties to support
                  resolutions
                </li>
              </ol>
            </div>

            <div class="subsection">
              <h4>Giving constructive feedback</h4>
              <ol class="numbered-list">
                <li>Give the feedback in private</li>
                <li>Remind them of what you already appreciate of them</li>
                <li>
                  Describe specific behaviors that were directly observed (not
                  inferred) and can be changed, e.g. "did not study the
                  documentation" versus "not prepared"
                </li>
                <li>Describe the impact of said behaviors</li>
                <li>
                  Point it out as an opportunity for growth rather than a fault
                </li>
                <li>Offer actionable suggestions</li>
              </ol>
            </div>

            <p>
              Although there seem to be a large number of required traits, you
              can cover 90% of them with a very common situation that occurs in
              engineering teams:
            </p>

            <ul class="bullet-list">
              <li>
                You had to work within a cross-functional team (e.g. with
                business stakeholders or product managers or designers) on a
                high pressure situation, where priorities and requirements were
                changing rapidly
              </li>
              <li>
                There's a conflict of interest between business/design and
                engineering, such as business/design pushing (demanding) for
                tighter deadlines, while from engineering perspective rushing
                for those deadlines will result in large tech debt
              </li>
              <li>
                You had to provide constructive feedback to business/design on
                the problem
              </li>
              <li>
                In so doing, you needed to convey technical concepts to the
                non-technical audience
              </li>
              <li>
                Further, you solicited feedback from them to understand how
                engineering could collaborate better to avoid this problem
                again. In so doing, you regularly ran through the list of
                demands or requirements with them to see if any could be dropped
                for faster engineering delivery. Additionally, you catered for
                their need on timeline accountability by providing regular
                updates
              </li>
            </ul>

            <p>Add in specific details according to your situation.</p>
