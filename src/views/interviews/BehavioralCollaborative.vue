            <h2>Sample story</h2>

            <p>Here's a sample story using the STAR format.</p>

            <div class="subsection">
              <h4>Situation</h4>
              <p>
                In my current job as front end engineer in a startup, I had to
                lead development on a highly urgent marketing project, working
                cross functionally with marketing and design. At some point,
                marketing had expected the feature to ship within the next week,
                but there was a significant roadblock from engineering due to
                dependencies on a partner API team whose deliverables were
                delayed due to recent departures of two senior members. The
                situation had become heated as marketing did not understand the
                roadblocks engineering had faced.
              </p>
            </div>

            <div class="subsection">
              <h4>Task</h4>
              <p>
                I had to resolve the misunderstanding to ensure smooth working
                relationship between both teams.
              </p>
            </div>

            <div class="subsection">
              <h4>Actions</h4>
              <p>
                In order to do so, I spoke to marketing privately and gave them
                time to clearly explain the urgency and concerns or
                misconceptions marketing might have towards engineering. I then
                explained the role of the partner teams, focusing specifically
                on their impact on the feature as opposed to complex technical
                information. In doing so, we were able to brainstorm different
                ways we could ship the feature even without the external
                dependencies. In parallel, I also spoke to the partner team's
                manager on the importance of the project to the company and the
                possibility of reprioritizing their work to help unblock us.
                Apart from that, I also solicited feedback on how engineering
                could better collaborate with marketing to avoid such
                misunderstandings in future.
              </p>
            </div>

            <div class="subsection">
              <h4>Results</h4>
              <p>
                Due to the tight collaboration between engineering and
                marketing, we were able to ship the feature in a timely manner.
                As marketing prioritized timelines and accountability, we
                started providing regular updates and discussed alternatives for
                every potential blocker. With this, every feature was
                successfully and smoothly delivered there after.
              </p>
            </div>
          </section>

