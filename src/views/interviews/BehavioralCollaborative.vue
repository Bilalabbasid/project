            behavioral questions asked in front end behavioral interviews, as
            they encompass a large number of related traits which can be grouped
            for ease of story/project preparation.
          </p>

          <section>
            <h2>Evaluation criteria in detail</h2>

            <p>
              When rating candidates under this category, interviewers are often
              looking at the following criteria:
            </p>

            <ul class="bullet-list">
              <li>Handling disagreements</li>
              <li>Working as a team</li>
              <li>Working with diverse personalities and skills</li>
              <li>Conveying complex ideas simply</li>
              <li>Giving constructive feedback</li>
              <li>Active listening</li>
            </ul>
