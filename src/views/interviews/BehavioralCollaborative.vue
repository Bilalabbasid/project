          <section>
            <h2>Abstracting collaboration questions</h2>

            <p>
              As mentioned in our behavioral interview preparation overview, it
              is impractical to prepare answers specifically for every
              behavioral question out there. However, by batching specific
              questions into similar themes and preparing stories that cover a
              large number of question requirements, we can reduce the number of
              stories to prepare to around 3-5 stories.
            </p>

            <p>
              Collaboration is such a theme which can group sub-requirements
              like communication, team work, adaptability and coachability. For
              each sub-requirement, we have summarized the common questions and
              also abstracted out the traits that interviewers are looking out
              for across these questions:
            </p>

            <div class="subsection">
              <h3>Communication</h3>

              <h4>Example questions</h4>
              <ul class="bullet-list">
                <li>
                  Can you describe a time when you had to effectively
                  communicate technical information to a non-technical audience
                  and how you approached it?
                </li>
                <li>
                  Can you describe a time when you had to adapt your
                  communication style to effectively communicate with someone
                  who had a different background or perspective?
                </li>
                <li>
                  Can you give an example of a time when you had to communicate
                  important information under time pressure or in a high-stress
                  situation?
                </li>
                <li>
                  How do you ensure that your message is understood and
                  well-received by your audience?
                </li>
              </ul>

              <h4>Traits interviewers look out for</h4>
              <ul class="bullet-list">
                <li>Convey complex ideas simply</li>
                <li>Active listening</li>
              </ul>
            </div>

            <div class="subsection">
              <h3>Teamwork</h3>

              <h4>Example questions</h4>
              <ul class="bullet-list">
                <li>
                  Can you describe a past project where you had to work with a
                  difficult stakeholder or teammate and how you dealt with it?
                </li>
                <li>
                  Can you tell me about a time when you had to give constructive
                  feedback to a team member or coworker?
                </li>
                <li>
                  How do you approach working with teammates who are not meeting
                  their deadlines or responsibilities?
                </li>
                <li>
                  Can you provide an example when you had to work with a team
                  where there were disagreements between members?
                </li>
                <li>
                  How do you handle working on a team with people who have
                  different skill sets and personalities?
                </li>
                <li>
                  Can you give an example of a time when you had to adapt to a
                  team member's working style in order to complete a project
                  successfully?
                </li>
                <li>
                  How do you ensure that all team members are heard and their
                  ideas are considered in group discussions?
                </li>
                <li>
                  Can you give an example of a time when you had to work on a
                  team project and had to compromise with others to reach a
                  solution?
                </li>
                <li>
                  Can you describe a time when you had to collaborate with
                  people from different departments or organizations to complete
                  a project?
                </li>
              </ul>

              <h4>Traits interviewers look out for</h4>
              <ul class="bullet-list">
                <li>Working as a team</li>
                <li>
                  Working with diverse working styles, skills and personalities
                </li>
                <li>Handling disagreements (others and own)</li>
                <li>Giving constructive feedback</li>
              </ul>
            </div>
