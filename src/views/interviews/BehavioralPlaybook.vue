                experiences. Make a list of 5-10 significant accomplishments and
                challenges from your career. For each one, note:
              </p>
              <ul class="bullet-list">
                <li>What was the situation?</li>
                <li>What was your role?</li>
                <li>What actions did you take?</li>
                <li>What were the results?</li>
                <li>What did you learn?</li>
              </ul>
            </div>

            <div class="principle-section">
              <h4>2. Practice common questions</h4>
              <p>
                Prepare answers for the most common behavioral questions.
                Practice speaking out loud and time yourself to ensure your
                answers are concise (1-2 minutes each).
              </p>
            </div>

            <div class="principle-section">
              <h4>3. Use the STAR method</h4>
              <p>
                Structure all your answers using the STAR framework. This
