<template>
  <div class="playbook-article-page">
    <div class="article-layout">
      <BehavioralPlaybookSidebar />
      <div class="article-main">
        <header class="article-header">
          <div class="article-meta">
            <span class="article-number">3.3</span>
            <span class="reading-time">6 min read</span>
          </div>
          <h1>Questions to Ask (End of Interview)</h1>
          <p class="article-subtitle">
            Strategic questions that demonstrate your interest and help you
            evaluate the opportunity
          </p>
        </header>
        <article class="article-content">
          <p class="lead-paragraph">
            In our anxiety to secure a job, some of us might forget that joining
            a bad team might be worse than waiting it out for a good
            opportunity. An interview is in fact an invaluable opportunity for
            the candidate to speak to someone who actually works in the company
            and find out more about the practices there. Few key aspects you
            should evaluate about a job before joining:
          </p>

          <ul class="bullet-list">
            <li>Will the work be interesting to you?</li>
            <li>Is there room for career growth?</li>
            <li>
              Are the teammates/your manager collaborative and easy to work
              with?
            </li>
          </ul>

          <p>
            Good questions asked at the end of the interview help a candidate
            achieve 2 objectives – leave a good impression and help the
            candidate assess if the company is truly suitable for them.
          </p>

          <p>
            In this article, we have provided a large list of useful questions
            you might ask to achieve this. Questions are organized by
            interviewer/interview round as well as objective.
          </p>

          <section>
            <h2>Screening rounds with HR/recruiters</h2>

            <p>
              The recruiter is usually not working directly in the team who is
              hiring you. Hence, it could be less insightful to ask questions
              about the team's day-to-day or on-the-ground situation. However,
              you could still find out information that recruiters would know,
              such as the candidate evaluation criteria or general company
              policies.
            </p>

            <div class="subsection">
              <h3>
                Get to know the criteria the hiring team is looking out for
              </h3>

              <p>
                These questions are very useful to understand what the hiring
                team is prioritizing, which they would probably have
                communicated with HR/recruiters beforehand so that they could
                help screen candidates accordingly.
              </p>

              <ul class="bullet-list">
                <li>
                  <strong
                    >"What are the skills, characteristics or experiences that
                    the team is looking for in an ideal candidate?"</strong
                  >
                  - These are invaluable as you would be able to pinpoint the
                  key criteria the team is prioritizing in hiring, which allows
                  you to specifically showcase those required skills,
                  characteristics or experiences in the next rounds with the
                  hiring team.
                </li>
                <li>
                  <strong
                    >"When is the anticipated starting date for this
                    position?"</strong
                  >
                  - More often than not, starting dates could be a key criteria
                  when a position has to be filled urgently. You could check
                  this with the recruiter early if you have a delayed start
                  date, to avoid wasting their time and yours.
                </li>
              </ul>
            </div>

            <div class="subsection">
              <h3>Get to know more about the hiring team</h3>

              <ul class="bullet-list">
                <li>
                  <strong
                    >"Will I have the opportunity to meet my potential manager
                    or colleagues during the interview process?"</strong
                  >
                  - If you were to meet the hiring team within the process, it
                  would be good to know which rounds it would be, and if
                  possible, who's who. This will help you prepare relevant
                  questions or conduct background checks beforehand.
                </li>
                <li>
                  <strong
                    >"Can you tell me about the team I'll be working with
                    directly, including who I will report to and the team's
                    structure?"</strong
                  >
                  - This question may or may not be useful depending on how
                  familiar the recruiter is with the hiring team (sometimes not
                  at all). However, you might still be able to get good insights
                  on the structure or dynamics of the team.
                </li>
              </ul>
            </div>

            <div class="subsection">
              <h3>Get to know more from the company's perspective</h3>

              <ul class="bullet-list">
                <li>
                  <strong
                    >"What are the current goals that the company is focused on,
                    and how does this team work to support hitting those
                    goals?"</strong
                  >
                  - This question gives you insights on the importance and
                  position of the team within the company, including how the
                  team contributes to the company's key goals. This could
                  provide critical insights on the team's key deliverables and
                  priorities, and correspondingly how you could best serve the
                  team. In bad times, lower priority teams could also be laid
                  off.
                </li>
                <li>
                  <strong
                    >"Can you give me some examples of the most and least
                    desirable aspects of the company's culture?"</strong
                  >
                  - While HR/recruiting might not be familiar with the team's
                  day-to-day, they would most likely know about the company's
                  policies and culture. This could give you some useful insights
                  which could also carry over to the next interview.
                </li>
                <li>
                  <strong
                    >"How frequently do employees make themselves available
                    outside of working hours"</strong
                  >
                  - In a similar vein, we want to know if there is a general
                  company culture for or against overtime work (without being
                  too obvious). This could be a deal-breaker for some people,
                  and should be cleared up asap.
                </li>
              </ul>
            </div>
          </section>

          <section>
            <h2>Rounds with the hiring team/team you will join</h2>

            <p>
              As a front end developer / web developer, rounds with the
              engineering team usually means rounds with the hiring team itself.
            </p>

            <p>
              Questions to ask in these rounds are highly critical in
              elucidating the experience you would likely get when you actually
              join the team (Note: This section is focused on questions to ask
              your potential team mates except manager. Questions to ask your
              potential manager are in the next section).
            </p>

            <div class="subsection">
              <h3>
                Check if the day-to-day realities of the job aligns with your
                goals
              </h3>

              <ul class="bullet-list">
                <li>
                  <strong
                    >"Can you show me examples of projects I'd be working on and
                    the technologies I will use? Will I be maintaining projects
                    or building from scratch?"</strong
                  >
                  - This question is useful to elucidate the exact kind of work
                  you would be doing, as well as some of the technologies used.
                  In general, having the opportunity to build products from
                  ground up could be a better learning opportunity than doing
                  small, incremental features.
                </li>
                <li>
                  <strong
                    >"Can you describe a typical day or week for a developer in
                    your team?" / "Can you walk me through the process of a
                    typical project"</strong
                  >
                  - This question helps to clarify quite a lot of things - the
                  kinds of tasks that a developer might be expected to do,
                  stakeholders that you will work with, as well as the project
                  management process and lifecycle.
                </li>
                <li>
                  <strong
                    >"What are some of the challenges you expect the person in
                    this position to face?"</strong
                  >
                  - This question is useful in uncovering some potential issues
                  in the codebase or workflows which could be a dealbreaker for
                  certain people. It might also uncover dysfunctional teams or
                  processes.
                </li>
                <li>
                  <strong
                    >"What does it look like during the busiest and toughest
                    times for this role?"</strong
                  >
                  - Asking this question could help uncover unreasonably high
                  expectations or pressure for the role, elucidating more about
                  work life balance without explicitly asking about it.
                </li>
                <li>
                  <strong
                    >"Will there be opportunities for stretch assignments where
                    I can learn and use new skills?"</strong
                  >
                  - If you're someone who seeks to grow your skills or portfolio
                  in the role, be sure to ask this question to know if you will
                  have reasonable autonomy to work on more complex projects on
                  request.
                </li>
              </ul>
            </div>

            <div class="subsection">
              <h3>
                Check if your potential manager's managerial style is
                appropriate for your growth
              </h3>

              <ul class="bullet-list">
                <li>
                  <strong
                    >What type of information is shared with employees? Is
                    revenue, profits, expenses, salary ranges, etc.
                    shared?</strong
                  >
                  - This question differentiates companies by their levels of
                  transparency. Some candidates might find this important, as it
                  could be frustrating to have little information and hence
                  little control over your compensation and promotion schedule.
                </li>
                <li>
                  <strong
                    >Who I will I report to? Can you tell me more about their
                    style of supervision?</strong
                  >
                  - This question elucidates if your potential manager is
                  someone who is hands-off or tends to look into the details of
                  your work. This could have a large impact on your experience
                  on the job.
                </li>
              </ul>
            </div>

            <div class="subsection">
              <h3>Understand more about the team dynamics and culture</h3>

              <ul class="bullet-list">
                <li>
                  <strong
                    >"What is the team structure and how does this role fit into
                    the team?"</strong
                  >
                  - This question is useful as it helps you to paint a picture
                  of the role in context to the overall team structure, how big
                  the team is and roughly what each part of the team does.
                </li>
                <li>
                  <strong
                    >"How would you describe the work environment here—is the
                    work typically more collaborative or more
                    independent?"</strong
                  >
                  - This question indirectly elucidates the likely culture of
                  the team - do people work in silos or do they communicate
                  frequently with one another.
                </li>
                <li>
                  <strong
                    >"Can you give me some examples of the most and least
                    desirable aspects of the team's culture?"</strong
                  >
                  - Another open-ended, useful question which allows you to gain
                  more insights which may not surface from a more general ask.
                </li>
                <li>
                  <strong
                    >"How frequently do team members make themselves available
                    outside of business hours?"</strong
                  >
                  - This question indirectly elucidates the culture of the team
                  with respect to work life balance.
                </li>
              </ul>
            </div>
          </section>

          <section>
            <h2>Rounds with your potential manager</h2>

            <p>
              In these rounds, it is useful to take the chance to find out your
              manager's managerial style, the performance review
              process/criteria, as well as his/her expectations of the role
              (which could give you an idea of how well you might perform on the
              job)
            </p>

            <ul class="bullet-list">
              <li>
                Can you tell me about the kind of supervision you provide?
              </li>
              <li>
                Thinking back to people you've seen do this work previously,
                what differentiated the ones who were good from the ones who
                were really great at it?
              </li>
              <li>
                What are you hoping this person will accomplish in their first
                six months and in their first year?
              </li>
              <li>
                What has been the top accomplishments of the organization over
                the past year?
              </li>
            </ul>
          </section>

          <section>
            <h2>
              Rounds with roles outside the team (who might not know
              specifically about team's operations)
            </h2>

            <div class="subsection">
              <h3>Questions to ask the founder</h3>

              <ul class="bullet-list">
                <li>
                  What are the current goals that the company is focused on, and
                  how does this team work to support hitting those goals?
                </li>
                <li>
                  What direction do you see this company heading in over the
                  next few years?
                </li>
                <li>
                  What are the current focus areas for the company as a whole?
                </li>
              </ul>
            </div>

            <div class="subsection">
              <h3>Questions to ask the team's collaborators</h3>

              <p>
                It is common to interview with someone you may not work directly
                with. In these cases, its best to cast a wider net and ask
                questions about the general company policy or culture. You could
                still ask them about your team if they are close collaborators.
              </p>

              <ul class="bullet-list">
                <li>
                  What's one challenge you occasionally or regularly face in
                  your job?
                </li>
                <li>Can you tell me about the team I'll be working with?</li>
                <li>
                  What learning and professional development opportunities are
                  available in the company?
                </li>
              </ul>
            </div>
          </section>

          <section>
            <h2>Questions to avoid</h2>

            <p>
              Here are a few examples of what not to ask at the end of your
              interview:
            </p>

            <div class="subsection">
              <h3>Avoid asking about salary and benefits early on</h3>

              <p>
                Only start discussing salary when the team is already keen on
                hiring you (i.e. at the end of the process), unless they ask you
                about your salary requirements.
              </p>

              <ul class="bullet-list">
                <li>What's the starting salary?</li>
                <li>Can you tell me about your health insurance?</li>
                <li>What are your paid leave policies?</li>
                <li>
                  If I get the job, when can I take time off for vacation?
                </li>
                <li>How often do you give out raises?</li>
                <li>How often do you hand out bonuses?</li>
                <li>What kind of perks and benefits can I expect?</li>
              </ul>
            </div>

            <div class="subsection">
              <h3>Avoid asking questions that should be public information</h3>

              <ul class="bullet-list">
                <li>What does the company do?</li>
                <li>Where are the offices of the company located?</li>
                <li>Who is the CEO of the company?</li>
              </ul>
            </div>
          </section>
