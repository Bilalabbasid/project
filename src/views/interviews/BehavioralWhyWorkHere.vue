          <section>
            <h2>The interviewer's intention</h2>

            <p>
              Most interviewers ask this question with a single objective in
              mind — to understand more about you through your motivations to
              join the company. They want to know whether you were interested
              enough to do your research, or if there was a special reason that
              constituted a strong motivation from you.
            </p>

            <p>
              Most candidates mention about how the company/job/product
              interests them and then stop there. However, a job is a mutually
              beneficial agreement. To leave a deeper impression, also mention
              why your skills will be useful to the company and how you can
              contribute.
            </p>
          </section>

          <section>
            <h2>Suggested answer structure</h2>

            <p>To succeed, there are 3 basic steps to build your answer:</p>

            <div class="subsection">
              <h3>1. It should take between 60 to 90 seconds</h3>

              <p>
                Never take more than 60 to 90 seconds. Any longer might
                constitute a monologue. For reference, 1 min is around 15
                statements.
              </p>
            </div>

            <div class="subsection">
              <h3>
                2. Gather deep insights about the company/team/position gained
                from current/previous employees of the company
              </h3>

              <p>
                If you've been an interviewer before, you'd know that most
                candidates do not impress in the "why do you want to work in
                this company / team" question because they tend to answer it
                based the company's public information, which may not
                necessarily be representative of the individual team or
                department's culture or situation.
              </p>

              <p>
                For instance, imagine you are evaluating 2 candidates for a game
                development team:
              </p>

              <ol class="numbered-list">
                <li>
                  Candidate A: "Company A is very environmentally friendly and I
                  want to be a part of the earth-saving efforts" (as the team's
                  manager, you know none of your team members ever talk about
                  earth-saving as your work is completely unrelated to it)
                </li>
                <li>
                  Candidate B: "Team A is working on games based on Technology X
                  and this is something I happen to be very interested in."
                </li>
              </ol>

              <p>
                To stand out as someone who is truly interested in the role, you
                should optimally strive to find insights specific to the team
                you are joining, and use that to justify your interest (and
                therefore fit).
              </p>

              <div class="principle-section">
                <h4>Common avenues to get team-specific insights:</h4>
                <ol class="numbered-list">
                  <li>
                    Gain more insights about the team's stack or practices from
                    the job description. In some cases, there could be many
                    details you could tap on
                  </li>
                  <li>
                    Search up team members on LinkedIn and understand more about
                    their individual work scope, past projects, backgrounds and
                    interests
                  </li>
                  <li>
                    Search up team members on Blind or even Reddit (go as
                    specific in your query as possible)
                  </li>
                  <li>
                    Ask the recruiters / hiring managers. Some companies will
                    have you chat with the hiring managers to assess suitability
                    even before any technical rounds are conducted. This is the
                    best chance to ask questions about the team and role
                  </li>
                </ol>
              </div>

              <div class="principle-section">
                <h4>Useful questions you could ask:</h4>
                <ol class="numbered-list">
                  <li>
                    What are some of the unique challenges or learning
                    opportunities that developers get on your team versus other
                    companies?
                  </li>
                  <li>
                    What are the kinds of projects I could expect to work on if
                    I were to join your team?
                  </li>
                  <li>
                    How does the way this team operates differ from other teams
                    in the company?
                  </li>
                  <li>
                    Does this team work closely with other teams? Which ones and
                    how so?
                  </li>
                  <li>
                    Can you walk me through the process of a typical project?
                  </li>
                </ol>
              </div>
            </div>

            <div class="subsection">
              <h3>
                3. Briefly explain why joining this company/role/team is a
                strategic and logical step for your career
              </h3>

              <p>
                In so doing, you could also take the chance to cement the
                interviewer's impression of your passion for the field or the
                efforts you have made to build your expertise.
              </p>
            </div>

            <div class="subsection">
              <h3>4. Explain what you bring to the table</h3>

              <p>
                Explain how you can contribute to the company through your
                skills and experience, aim to be unique and memorable if
                possible.
              </p>
            </div>
          </section>

          <section>
            <h2>Example questions and answers</h2>

            <div class="subsection">
              <h3>"Why do you want to work in the TikTok Shop team?"</h3>

              <div class="example-section">
                <div class="sample-response">
                  <p>
                    "I've worked as a web developer while in my previous company
                    on an e-commerce platform for almost 4 years. Having gained
                    significant experience as an individual contributor, the
                    next step for me is to work on larger, more complex projects
                    within a bigger team to take my development skills to the
                    next level.
                  </p>
                  <p>The top 3 things about TikTok Shop for me are:</p>
                  <ol class="numbered-list">
                    <li>
                      I like to know that my development efforts are able to
                      tangibly drive impact. At the current stage, TikTok Shop
                      is still at a relatively nascent stage of the product
                      lifecycle where that is possible.
                    </li>
                    <li>
                      TikTok Shop serves the wide masses and has to deal with
                      associated challenges such as accessibility, localization,
                      and e-commerce in a non-traditional format, which allows
                      me to develop my skills in that area.
                    </li>
                    <li>
                      ByteDance (TikTok's parent company) internally develops
                      many cutting edge technologies and also uses open source
                      technologies. I'm excited to learn from the company's
                      experienced engineers on how to scale these technologies
                      to a worldwide audience.
                    </li>
                  </ol>
                  <p>
                    Lastly, my prior experience with e-commerce platforms would
                    help me get up to speed fast and I'm eager to contribute to
                    evolving TikTok's e-commerce platform to tackle future
                    challenges."
                  </p>
                </div>
              </div>
            </div>
          </section>
