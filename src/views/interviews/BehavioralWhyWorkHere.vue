          </p>
        </header>
        <article class="article-content">
          <p class="lead-paragraph">
            As a hiring manager, "Why do you want to work here" is an extremely
            common question that is probably asked in over 70% of interviews.
            Yet, most candidates fail to answer this well. In this article, you
            will learn from ex-interviewers how to answer this question in an
            effective and concise manner.
          </p>

          <p>In this article you will find:</p>

